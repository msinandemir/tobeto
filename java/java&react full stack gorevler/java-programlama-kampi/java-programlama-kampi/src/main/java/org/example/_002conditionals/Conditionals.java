package org.example._002conditionals;

public class Conditionals {
    public static void main(String[] args) {
        int not = 35;
        if (not >= 50)
            System.out.println("Geçti");
        else if (not >= 40)
            System.out.println("Bütünleme");
        else
            System.out.println("Kaldı");
    }
}
